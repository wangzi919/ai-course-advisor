# 建立代碼到名稱的映射
DEPT_CODE_TO_NAME = {code: name for code, name in DEPARTMENTS}

# 建立名稱到代碼的映射
DEPT_NAME_TO_CODE = {name: code for code, name in DEPARTMENTS}


def get_all_codes():
    """取得所有系所代碼"""
    return [code for code, name in DEPARTMENTS]


def get_dept_name(code):
    """根據代碼取得系所名稱"""
    return DEPT_CODE_TO_NAME.get(code, None)


def get_dept_code(name):
    """根據名稱取得系所代碼"""
    return DEPT_NAME_TO_CODE.get(name, None)


def get_career(dept_code):
    """根據系所代碼取得對應的 career 值"""
    prefix = dept_code[0]
    career_map = {
        'C': 'C',  # 學院
        'U': 'U',  # 學士班
        'B': 'B',  # 研究所(學院)
        'G': 'G',  # 碩士班
        'D': 'D',  # 博士班
        'R': 'R',  # 產業碩士專班
        'W': 'W',  # 碩士在職專班
        'N': 'N',  # 進修學士班
    }
    return career_map.get(prefix, 'U')


